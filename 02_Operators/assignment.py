# assignment_operators.py

# 1. Basic assignment
a = 10
print("1. =  →", a)  # Assigns value

# 2. Add AND
a += 5
print("2. += →", a)  # a = a + 5

# 3. Subtract AND
a -= 3
print("3. -= →", a)  # a = a - 3

# 4. Multiply AND
a *= 2
print("4. *= →", a)  # a = a * 2

# 5. Divide AND
a /= 4
print("5. /= →", a)  # a = a / 4

# 6. Modulus AND
a %= 3
print("6. %= →", a)  # a = a % 3

# 7. Floor Division AND
a = 10
a //= 3
print("7. //= →", a)  # a = a // 3

# 8. Exponent AND
a **= 3
print("8. **= →", a)  # a = a ** 3

# 9. Bitwise AND assignment
a = 5
a &= 3
print("9. &= →", a)  # a = a & 3

# 10. Bitwise OR assignment
a |= 2
print("10. |= →", a)  # a = a | 2

# 11. Bitwise XOR assignment
a ^= 1
print("11. ^= →", a)  # a = a ^ 1

# 12. Bitwise right shift AND
a = 8
a >>= 2
print("12. >>= →", a)  # a = a >> 2

# 13. Bitwise left shift AND
a <<= 3
print("13. <<= →", a)  # a = a << 3
