# and Operator (Logical AND)
age = 20
citizen = True

if age >= 18 and citizen:
    print('Eligible to vote')
else:
    print('Not eligible')


# or Operator (Logical OR)
marks = 45
if marks < 50 or marks > 90:
    print('Special attention needed');


# not Operator (Logical NOT)
is_logged_in = False
if not is_logged_in:
    print('Please log in first');