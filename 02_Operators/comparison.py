# == (Equal To)
x = 10
print(x == 10) # True
print(x == 5) # False

# != (Not Equal To)
x = 20
print(x != 5) # True
print(x != 20) # False

# > (Greater Than)
age = 20
print(age > 18) # True


# < (Less Than)
marks = 45;
print(marks < 50) # True

# >= (Greater Than or Equal)
score = 90
print(score >= 90) # True

# <= (Less Than or Equal)
temperature = 25
print(temperature <= 30) # True